package org.example.e24;

public class E24BooleanCheckIfConditions {
    public static void main(String[] args) {
        // Step 1: Declare a boolean variable called `isJavaFun` and assign the value `true`
        boolean isJavaFun = true;

        // Step 2: Check if the boolean is true using if-else condition
        if (isJavaFun) {
            System.out.println("Java is fun!");
        } else {
            System.out.println("Java is not fun.");
        }
    }
}