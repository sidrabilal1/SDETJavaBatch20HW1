package org.example.e6;

public class E6ArithmeticOperations {
    public static void main(String[] args) {

        // Step 1: Create a float variable and assign a value of 8.2 to it
        float number = 8.2f;

        // Step 2: Multiply the value by itself and store the result in a new variable
        float result = number * number;

        // Step 3: Print the result in the specified format
        System.out.println("The multiplication of " + number + " with itself is equal to " + result);
    }

}


