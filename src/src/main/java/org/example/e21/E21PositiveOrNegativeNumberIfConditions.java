package org.example.e21;

public class E21PositiveOrNegativeNumberIfConditions {
    public static void main(String[] args) {
        // Step 1: Declare an integer variable called `number` and assign the value -5
        int number = -5;

        // Step 2: Check if the number is positive or negative using if-else condition
        if (number > 0) {
            System.out.println("The number is positive.");
        } else {
            System.out.println("The number is negative.");
        }
    }
}