package org.example.e7;

public class E7ArithmeticOperations {
    public static void main(String[] args) {
        // Step 1: Declare integer variables and assign values
        int num1 = 200;
        int num2 = 100;

        // Step 2: Perform arithmetic operations
        int sum = num1 + num2;
        int difference = num1 - num2;
        int product = num1 * num2;
        int quotient = num1 / num2;

        // Step 3: Print the results
        System.out.println(sum);        // Expected output: 300
        System.out.println(difference); // Expected output: 100
        System.out.println(product);    // Expected output: 20000
        System.out.println(quotient);   // Expected output: 2

    }
}
