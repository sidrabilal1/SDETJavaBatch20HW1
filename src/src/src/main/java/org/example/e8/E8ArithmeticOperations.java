package org.example.e8;

public class E8ArithmeticOperations {
    public static void main(String[] args) {
        // Step 1: Declare integer variables and assign values
        int width = 5;
        int height = 8;

        // Step 2: Calculate the perimeter of the rectangle
        int perimeter = 2 * (width + height);

        // Step 3: Calculate the area of the rectangle
        int area = width * height;

        // Step 4: Print the perimeter and area in the specified format
        System.out.println("The perimeter of a rectangle with width " + width + " and height " + height + " is equal to " + perimeter + " and the area equals to " + area);
    }
}
