package org.example.e9;

public class E9StringConcatenation {
    public static void main(String[] args) {
        // Step 1: Create a variable and assign the value "John" to it
        String firstName = "John";

        // Step 2: Create a variable and assign the value "Smith" to it
        String lastName = "Smith";

        // Step 3: Print the concatenated value of the variables 'firstName' and 'lastName' with a space in between
        System.out.println(firstName + " " + lastName);
    }
}
