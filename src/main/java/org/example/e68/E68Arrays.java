package org.example.e68;

public class E68Arrays {
    public static void main(String[] args) {
        char[] characters = {'s', 'a', 'y', 'b', 'n', 'c', 't', 'd', 'a', 'e', 'x'};

        System.out.print(characters[0]); // s
        System.out.print(characters[2]); // y
        System.out.print(characters[4]); // n
        System.out.print(characters[6]); // t
        System.out.print(characters[1]); // a
        System.out.print(characters[10]); // x





    }
}



