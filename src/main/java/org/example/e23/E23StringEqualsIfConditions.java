package org.example.e23;

public class E23StringEqualsIfConditions {
    public static void main(String[] args) {
        // Step 1: Declare a string variable called `language` and assign the value "Java"
        String language = "Java";

        // Step 2: Check if the string equals "Java" using if-else condition
        if (language.equals("Java")) {
            System.out.println("The programming language is Java.");
        } else {
            System.out.println("The programming language is not Java.");
        }
    }
}